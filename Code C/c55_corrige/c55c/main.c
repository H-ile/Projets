#include <stdio.h>
#include <stdlib.h>

int main() {

  // Variables(s)
  int i=0;

  // Traitement(s)

  // Afficher tous les entiers de 1 à 100 par pas de 10 avec for
  printf("\n\n->Afficher tous les entiers de 1 a 10 avec for\n");
  for (i=10; i<=100; i=i+10) {
    printf("%d ",i);
  }

  // Fin
  printf("\n\n That's all folks\n");
  return 0;
}
