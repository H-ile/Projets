#include <stdio.h>
#include <stdlib.h>

int main() {

  // Variables(s)
  int i=0;

  // Traitement(s)

  // Afficher tous les entiers de 1 à 10 avec while
  printf("\n\n->Afficher tous les entiers de 1 a 10 avec while\n");

  i=1;
  while (i<=10) {
    printf("%d ",i);
    i++;
  }

  // Fin
  printf("\n\n That's all folks\n");
  return 0;
}
